package doctor

import (
	"syscall"
)

// diskFreeBytes returns the number of free bytes available to the caller on
// the filesystem that contains path. Uses statfs(2) on Unix/macOS.
func diskFreeBytes(path string) (uint64, error) {
	var stat syscall.Statfs_t
	if err := syscall.Statfs(path, &stat); err != nil {
		return 0, err
	}
	// Bavail is the free blocks available to non-root users.
	return stat.Bavail * uint64(stat.Bsize), nil //nolint:unconvert
}
